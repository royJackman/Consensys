const NiFTyMintingContract = new web3.eth.Contract(NiFTyMintingContractABI, process.env.NIFTY_MINT_CONTRACT_ADDRESS);
const NiFTyLoaningContract = new web3.eth.Contract(NiFTyLoaningContractABI, process.env.NIFTY_LOAN_CONTRACT_ADDRESS);

export const mintNiFTy = (recipient: string, colorTL: string, colorTR: string, colorBL: string, colorBR: string) => {
    NiFTyMintingContract.methods.mint(recipient, colorTL, colorTR, colorBL, colorBR)
        .send({ from: recipient })
        .on('receipt', (receipt) => console.log('NiFTy minted successfully, congratulations!\n', receipt))
        .on('error', (err) => console.log('Error minting NiFTy: ', err));
}

export const loanNiFTy = (borrower: string, tokenId: number, loanAmount: number, duration: number, contractAddress: string) => {
    NiFTyLoaningContract.methods.takeOutLoan(contractAddress, tokenId, loanAmount, duration)
        .send({ from: borrower })
        .on('receipt', (receipt) => console.log('Loan collateralized successfully!\n', receipt))
        .on('error', (err) => console.log('Error taking out loan: ', err));
}

export const repayNiFTy = (borrower: string, loanId: number, repayAmount: number) => {
    NiFTyLoaningContract.methods.repayLoan(loanId)
        .send({ from: borrower, value: repayAmount.toString() })
        .on('receipt', (receipt) => console.log('Loan repaid successfully!\n', receipt))
        .on('error', (err) => console.log('Error repaying loan: ', err));
}